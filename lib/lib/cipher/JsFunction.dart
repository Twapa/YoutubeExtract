 class JsFunction {


     String va;
     String name;
     String argument;
     JsFunction(String va, String name, String argument) {
        this.va = va;
        this.name = name;
        this.argument = argument;
    }

   String getVar() {
        return va;
    }

     String getName() {
        return name;
    }

     String getArgument() {
        return argument;
    }
}
